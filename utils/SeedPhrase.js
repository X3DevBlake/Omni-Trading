export class SeedPhraseService {
    /**
     * Generate a 12-word seed phrase securely.
     */
    static generate() {
        const words = [];
        const entropy = new Uint16Array(12);
        crypto.getRandomValues(entropy);
        
        for (let i = 0; i < 12; i++) {
            const index = entropy[i] % BIP39_WORDLIST.length;
            words.push(BIP39_WORDLIST[index]);
        }
        return words.join(' ');
    }

    /**
     * Deterministically derive a 0x address from a seed phrase.
     * In production, this uses BIP32/BIP44 derivation.
     * Here we use a stable hash of the seed phrase for the sovereign demo.
     */
    static deriveAddress(seed) {
        let hash = 0;
        for (let i = 0; i < seed.length; i++) {
            hash = ((hash << 5) - hash) + seed.charCodeAt(i);
            hash |= 0;
        }
        const hex = Math.abs(hash).toString(16).padStart(40, '0');
        return '0x' + hex.substring(0, 40);
    }
}
